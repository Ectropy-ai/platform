#!/usr/bin/env node

/**
 * Current Truth Markdown Generator
 *
 * Generates docs/CURRENT_TRUTH.md from docs/current-truth.json
 * This ensures the markdown view is always in sync with the JSON source of truth.
 *
 * Usage:
 *   node scripts/docs/generate-current-truth-md.js
 *   npm run docs:generate
 *
 * Exit codes:
 *   0 - Generation successful
 *   1 - Generation failed
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

// Paths
const REPO_ROOT = path.resolve(__dirname, '../..');
const JSON_PATH = path.join(
  REPO_ROOT,
  'apps/mcp-server/data/current-truth.json'
);
const MD_PATH = path.join(REPO_ROOT, 'docs/CURRENT_TRUTH.md');

// ANSI color codes
const colors = {
  reset: '\x1b[0m',
  green: '\x1b[32m',
  red: '\x1b[31m',
  yellow: '\x1b[33m',
  blue: '\x1b[34m',
  bold: '\x1b[1m',
};

/**
 * Format output message
 */
function log(icon, message, color = colors.reset) {
  console.log(`${color}${icon} ${message}${colors.reset}`);
}

/**
 * Load JSON file with error handling
 */
function loadJSON(filePath) {
  try {
    const content = fs.readFileSync(filePath, 'utf8');
    return JSON.parse(content);
  } catch (error) {
    log(
      '❌',
      `Failed to load ${path.basename(filePath)}: ${error.message}`,
      colors.red
    );
    return null;
  }
}

/**
 * Format relationship type for human readability
 */
function formatRelationshipType(type) {
  const typeMap = {
    dependsOn: 'Depends on',
    enables: 'Enables',
    consumes: 'Consumes',
    produces: 'Produces',
    governs: 'Governs',
    guides: 'Guides',
    validates: 'Validates',
    enforces: 'Enforces',
    supports: 'Supports',
    motivates: 'Motivates',
    documents: 'Documents',
    supersedes: 'Supersedes',
    replaces: 'Replaces',
    extends: 'Extends',
    relates: 'Related to',
    diagnoses: 'Diagnoses',
    resolves: 'Resolves',
    mitigates: 'Mitigates',
    prevents: 'Prevents',
    conflicts: 'Conflicts with',
    implements: 'Implements',
    deploys: 'Deploys',
    triggers: 'Triggers',
    archives: 'Archives',
    contains: 'Contains',
    partOf: 'Part of',
    shares: 'Shares',
  };

  return typeMap[type] || type;
}

/**
 * Format node type for human readability
 */
function formatNodeType(type) {
  const typeMap = {
    deliverable: 'Deliverable',
    investigation: 'Investigation',
    infrastructure: 'Infrastructure',
    workflow: 'Workflow',
    decision: 'Decision',
    pattern: 'Pattern',
    issue: 'Issue',
    milestone: 'Milestone',
  };

  return typeMap[type] || type;
}

/**
 * Format priority for display
 */
function formatPriority(priority) {
  const priorityMap = {
    critical: '🔴 P0-Critical',
    high: '🟠 P1-High',
    medium: '🟡 P2-Medium',
    low: '🟢 P3-Low',
  };

  return priorityMap[priority] || priority;
}

/**
 * Format status for display
 */
function formatStatus(status) {
  const statusMap = {
    completed: '✅ COMPLETED',
    'in-progress': '🔄 IN PROGRESS',
    blocked: '🚫 BLOCKED',
    planned: '📋 PLANNED',
    archived: '📦 ARCHIVED',
  };

  return statusMap[status] || status.toUpperCase();
}

/**
 * Format timestamp for display
 */
function formatDate(timestamp) {
  const date = new Date(timestamp);
  const year = date.getFullYear();
  const month = String(date.getMonth() + 1).padStart(2, '0');
  const day = String(date.getDate()).padStart(2, '0');
  return `${year}-${month}-${day}`;
}

/**
 * Generate markdown header
 */
function generateHeader(json) {
  const now = new Date().toISOString();
  const lastUpdated = formatDate(json.lastUpdated);

  return `# Current Platform Truth

**Generated:** ${now}  
**Source:** current-truth.json (automated - DO NOT EDIT THIS FILE DIRECTLY)  
**Last Updated:** ${lastUpdated}

⚠️ **IMPORTANT: This file is auto-generated**

This markdown file is automatically generated from \`docs/current-truth.json\`.

**To make changes:**
1. Edit \`docs/current-truth.json\` directly
2. Validate: \`npm run docs:validate\`
3. Regenerate markdown: \`npm run docs:generate\`
4. Commit both JSON and markdown files

**Never edit CURRENT_TRUTH.md directly** - your changes will be overwritten on the next generation.

---

`;
}

/**
 * Generate platform state section
 */
function generatePlatformState(platformState, policies) {
  const md = `## Platform State

**Overall Health:** ${platformState.health}  
**Current Phase:** ${platformState.phase}  
**Progress:** ${platformState.progress.completedDeliverables}/${platformState.progress.totalDeliverables} deliverables completed  
**Active Workstreams:** ${platformState.progress.activeWorkstreams}

**Retention Policy:** ${policies.retention.defaultDays} days (default), ${policies.retention.extendedDays} days (extended)  
**Archive Schedule:** ${policies.retention.archiveSchedule}  
**Next Archive:** ${policies.archiving.nextArchiveDate}

---

`;

  return md;
}

/**
 * Generate a single node entry
 */
function generateNodeEntry(node) {
  let md = `### ${formatDate(node.timestamp)}: ${node.title}\n\n`;

  // Status and Priority line
  md += `**Status:** ${formatStatus(node.status)} | **Priority:** ${formatPriority(node.priority)} | **Type:** ${formatNodeType(node.nodeType)}\n\n`;

  // Context
  if (node.content.context) {
    md += `**Context:** ${node.content.context}\n\n`;
  }

  // Approach (handle both string and array)
  if (node.content.approach) {
    md += `**Approach:**\n`;
    if (Array.isArray(node.content.approach)) {
      node.content.approach.forEach((step) => {
        md += `- ${step}\n`;
      });
    } else {
      md += `${node.content.approach}\n`;
    }
    md += '\n';
  }

  // Outcome (handle both string and array)
  if (node.content.outcome) {
    md += `**Outcome:**\n`;
    if (Array.isArray(node.content.outcome)) {
      node.content.outcome.forEach((outcome) => {
        md += `- ${outcome}\n`;
      });
    } else {
      md += `${node.content.outcome}\n`;
    }
    md += '\n';
  }

  // Evidence
  if (node.content.evidence && node.content.evidence.length > 0) {
    md += `**Evidence:**\n`;
    node.content.evidence.forEach((evidence) => {
      md += `- \`${evidence}\`\n`;
    });
    md += '\n';
  }

  // Files Modified
  if (node.content.filesModified && node.content.filesModified.length > 0) {
    md += `**Files Modified:**\n`;
    node.content.filesModified.forEach((file) => {
      md += `- \`${file.path}\` - ${file.description}\n`;
    });
    md += '\n';
  }

  // Relationships
  if (node.relationships && Object.keys(node.relationships).length > 0) {
    const hasRelationships = Object.values(node.relationships).some(
      (arr) => arr && arr.length > 0
    );

    if (hasRelationships) {
      md += `**Related:**\n`;
      Object.entries(node.relationships).forEach(([type, targets]) => {
        if (targets && targets.length > 0) {
          md += `- ${formatRelationshipType(type)}: ${targets.join(', ')}\n`;
        }
      });
      md += '\n';
    }
  }

  // Labels/Tags
  if (node.labels && node.labels.length > 0) {
    md += `**Labels:** ${node.labels.map((l) => `\`${l}\``).join(', ')}\n\n`;
  }

  return md;
}

/**
 * Generate index section
 */
function generateIndex(index) {
  let md = `## Index\n\n`;

  // By Node Type
  if (index.byNodeType && Object.keys(index.byNodeType).length > 0) {
    md += `### By Node Type\n\n`;

    // Sort by count descending
    const typeEntries = Object.entries(index.byNodeType).sort(
      (a, b) => b[1].length - a[1].length
    );

    for (const [type, nodes] of typeEntries) {
      md += `- **${formatNodeType(type)}:** ${nodes.length} ${nodes.length === 1 ? 'entry' : 'entries'}\n`;
    }
    md += '\n';
  }

  // By Status
  if (index.byStatus && Object.keys(index.byStatus).length > 0) {
    md += `### By Status\n\n`;

    // Sort by count descending
    const statusEntries = Object.entries(index.byStatus).sort(
      (a, b) => b[1].length - a[1].length
    );

    for (const [status, nodes] of statusEntries) {
      md += `- **${formatStatus(status)}:** ${nodes.length} ${nodes.length === 1 ? 'entry' : 'entries'}\n`;
    }
    md += '\n';
  }

  // By Phase
  if (index.byPhase && Object.keys(index.byPhase).length > 0) {
    md += `### By Phase\n\n`;

    // Sort by phase name
    const phaseEntries = Object.entries(index.byPhase).sort((a, b) =>
      a[0].localeCompare(b[0])
    );

    for (const [phase, nodes] of phaseEntries) {
      md += `- **${phase}:** ${nodes.length} ${nodes.length === 1 ? 'entry' : 'entries'}\n`;
    }
    md += '\n';
  }

  // Top Tags
  if (index.byTag && Object.keys(index.byTag).length > 0) {
    md += `### Top Tags\n\n`;

    // Sort by count descending, take top 10
    const tagEntries = Object.entries(index.byTag)
      .sort((a, b) => b[1].length - a[1].length)
      .slice(0, 10);

    for (const [tag, nodes] of tagEntries) {
      md += `- **${tag}:** ${nodes.length} ${nodes.length === 1 ? 'entry' : 'entries'}\n`;
    }
    md += '\n';
  }

  return md;
}

/**
 * Main generation function
 */
function generateMarkdown() {
  console.log(
    `${colors.bold}${colors.blue}Current Truth Markdown Generator${colors.reset}\n`
  );

  // Check if JSON file exists
  if (!fs.existsSync(JSON_PATH)) {
    log('❌', `JSON file not found: ${JSON_PATH}`, colors.red);
    process.exit(1);
  }

  // Load JSON
  log('📂', 'Loading current-truth.json...', colors.blue);
  const json = loadJSON(JSON_PATH);

  if (!json) {
    process.exit(1);
  }

  log('✅', `Loaded ${json.nodes.length} nodes`, colors.green);

  // Generate markdown
  log('🔨', 'Generating markdown...', colors.blue);

  let markdown = '';

  // Header
  markdown += generateHeader(json);

  // Platform State
  markdown += generatePlatformState(json.platformState, json.policies);

  // Recent Changes
  markdown += `## Recent Changes\n\n`;

  // Sort nodes chronologically (newest first)
  const sortedNodes = [...json.nodes].sort(
    (a, b) => new Date(b.timestamp) - new Date(a.timestamp)
  );

  log('📝', `Processing ${sortedNodes.length} nodes...`, colors.blue);

  for (const node of sortedNodes) {
    markdown += generateNodeEntry(node);
    markdown += '---\n\n';
  }

  // Index
  markdown += generateIndex(json.index);

  // Footer
  markdown += `---\n\n`;
  markdown += `**Generated by:** \`scripts/docs/generate-current-truth-md.js\`  \n`;
  markdown += `**Source file:** \`docs/current-truth.json\`  \n`;
  markdown += `**Schema version:** ${json.schemaVersion}\n`;

  // Write to file
  try {
    fs.writeFileSync(MD_PATH, markdown, 'utf8');
    log('✅', `CURRENT_TRUTH.md generated successfully`, colors.green);
    log('📊', `Generated ${markdown.length} characters`, colors.blue);

    // Calculate file size in KB
    const sizeKB = (markdown.length / 1024).toFixed(2);
    log('📏', `File size: ${sizeKB} KB`, colors.blue);

    if (parseFloat(sizeKB) > 30) {
      log(
        '⚠️ ',
        `File exceeds 30KB target (see DOCUMENTATION_POLICY.md)`,
        colors.yellow
      );
    }
  } catch (error) {
    log('❌', `Failed to write markdown file: ${error.message}`, colors.red);
    process.exit(1);
  }

  console.log(
    `\n${colors.green}${colors.bold}✅ Markdown generation complete${colors.reset}`
  );
  process.exit(0);
}

// Run generator
generateMarkdown();
